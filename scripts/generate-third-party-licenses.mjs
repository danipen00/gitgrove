// Generates build/THIRD-PARTY-LICENSES.txt: an aggregated notice listing every
// production dependency that ships inside GitGrove, with its full license text
// and any NOTICE file. Apache-2.0 deps (@pierre/trees, @pierre/diffs) require us
// to redistribute their LICENSE and propagate their NOTICE; this keeps that
// automatic so the bundle never drifts from the installed dependency tree.
//
// Scope is the production dependency graph only (package.json `dependencies` and
// their transitive deps). devDependencies are build-time tooling and are not
// shipped. The Electron/Chromium/Node runtime carries its own license files
// inside the Electron framework, so it is intentionally not duplicated here.

import { existsSync, mkdirSync, readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const rootDir = join(dirname(fileURLToPath(import.meta.url)), '..')
const outFile = join(rootDir, 'build', 'THIRD-PARTY-LICENSES.txt')

const LICENSE_FILE = /^(licen[cs]e|copying)(\..+)?$/i
const NOTICE_FILE = /^notice(\..+)?$/i

/** Resolve a dependency's install directory, walking node_modules upward to
 *  mirror npm/bun hoisting, then falling back to the root node_modules. */
function resolvePkgDir(name, fromDir) {
  let dir = fromDir
  for (;;) {
    const candidate = join(dir, 'node_modules', name)
    if (existsSync(join(candidate, 'package.json'))) return candidate
    const parent = dirname(dir)
    if (parent === dir) break
    dir = parent
  }
  const atRoot = join(rootDir, 'node_modules', name)
  return existsSync(join(atRoot, 'package.json')) ? atRoot : null
}

function readNamed(dir, matcher) {
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    if (entry.isFile() && matcher.test(entry.name)) {
      return readFileSync(join(dir, entry.name), 'utf8').trim()
    }
  }
  return null
}

function licenseId(pkg) {
  if (typeof pkg.license === 'string') return pkg.license
  if (pkg.license?.type) return pkg.license.type
  if (Array.isArray(pkg.licenses)) {
    return pkg.licenses.map((l) => l.type || l).join(' / ')
  }
  return 'UNKNOWN'
}

function homepageOf(pkg) {
  if (pkg.homepage) return pkg.homepage
  const url = typeof pkg.repository === 'string' ? pkg.repository : pkg.repository?.url
  return url ? url.replace(/^git\+/, '').replace(/\.git$/, '') : null
}

const rootPkg = JSON.parse(readFileSync(join(rootDir, 'package.json'), 'utf8'))
const queue = Object.keys(rootPkg.dependencies ?? {}).map((name) => ({ name, fromDir: rootDir }))
const visited = new Set()
const collected = new Map()
const missing = []

while (queue.length > 0) {
  const { name, fromDir } = queue.shift()
  const dir = resolvePkgDir(name, fromDir)
  if (!dir) {
    missing.push(name)
    continue
  }
  if (visited.has(dir)) continue
  visited.add(dir)

  const pkg = JSON.parse(readFileSync(join(dir, 'package.json'), 'utf8'))
  const key = `${pkg.name}@${pkg.version}`
  if (!collected.has(key)) {
    collected.set(key, {
      name: pkg.name,
      version: pkg.version,
      license: licenseId(pkg),
      homepage: homepageOf(pkg),
      licenseText: readNamed(dir, LICENSE_FILE),
      noticeText: readNamed(dir, NOTICE_FILE)
    })
  }

  for (const dep of [
    ...Object.keys(pkg.dependencies ?? {}),
    ...Object.keys(pkg.optionalDependencies ?? {})
  ]) {
    queue.push({ name: dep, fromDir: dir })
  }
}

if (missing.length > 0) {
  console.warn(
    `Warning: could not resolve ${missing.length} dependency directory(ies): ${[...new Set(missing)].join(', ')}.\n` +
      'Run `bun install` so the full production tree is present before generating licenses.'
  )
}

const packages = [...collected.values()].sort((a, b) => a.name.localeCompare(b.name))
const sep = '='.repeat(80)

const lines = [
  'THIRD-PARTY SOFTWARE NOTICES AND LICENSES',
  '',
  `GitGrove bundles the following open-source packages. This file is generated by`,
  `scripts/generate-third-party-licenses.mjs from the installed production`,
  `dependency tree; do not edit it by hand.`,
  '',
  `Generated: ${new Date().toISOString().slice(0, 10)}`,
  `Packages: ${packages.length}`,
  '',
  sep,
  ''
]

for (const pkg of packages) {
  lines.push(`${pkg.name}@${pkg.version}`)
  lines.push(`License: ${pkg.license}`)
  if (pkg.homepage) lines.push(`Homepage: ${pkg.homepage}`)
  lines.push('')
  if (pkg.licenseText) {
    lines.push(pkg.licenseText)
  } else {
    lines.push(`(No license file found in the package; declared license: ${pkg.license}.)`)
  }
  if (pkg.noticeText) {
    lines.push('')
    lines.push('--- NOTICE ---')
    lines.push('')
    lines.push(pkg.noticeText)
  }
  lines.push('')
  lines.push(sep)
  lines.push('')
}

mkdirSync(dirname(outFile), { recursive: true })
writeFileSync(outFile, `${lines.join('\n').trimEnd()}\n`, 'utf8')
console.log(`Wrote ${outFile} (${packages.length} packages).`)
